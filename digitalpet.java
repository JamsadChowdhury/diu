public class DigitalPet {

    public static void main(String[] args) {

        String state = "Happy";

        System.out.println("Current State : " + state);

        state = "Sad";
        System.out.println("Punishment -> " + state);

        state = "Happy";
        System.out.println("Praise -> " + state);

        state = "Sad";
        System.out.println("Punishment -> " + state);

        state = "Heart Broken";
        System.out.println("Punishment Again -> " + state);

    }

}
